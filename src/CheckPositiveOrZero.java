public class CheckPositiveOrZero {
    public static void main(String[] args) {
       int num = -10;
       if(num == 0){
           System.out.println("Equal To Zero...");
       }
       else if(num < 0){
           System.out.println("Number is a negative number...");
       }
       else{
           System.out.println(" Is a positive number");
       }
    }
}
