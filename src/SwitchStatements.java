public class SwitchStatements {
    public static void main(String[] args) {
        String days = "Friday";
        switch(days){
            case "Sunday":
                System.out.println(1);
            case "Monday":
                System.out.println(2);
            case "Tuesday":
                System.out.println(3);
            case "Wednesday":
                System.out.println(4);
            case "Thursday":
                System.out.println(5);
            case "Friday":
                System.out.println(6);

        }
    }
}
